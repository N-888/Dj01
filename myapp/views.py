# Импортируем функцию HttpResponse из модуля django.http
# HttpResponse позволяет создать HTTP-ответ для отправки в браузер
from django.http import HttpResponse


# Создаем функцию-представление для страницы /data
# Функция принимает один параметр request - это объект запроса от браузера
def data_page(request):
    """
    Эта функция будет вызываться, когда пользователь перейдет на страницу /data
    Она возвращает HTTP-ответ с HTML-кодом страницы
    """

    # Возвращаем объект HttpResponse с HTML-кодом внутри
    # Каждая строка в тройных кавычках - это часть HTML-страницы
    return HttpResponse(
        # <h1> - заголовок первого уровня (самый крупный)
        '<h1>Страница с данными</h1>'
        # <p> - параграф текста
        '<p>Это специальная страница для отображения данных.</p>'
        # <ul> - неупорядоченный список (маркированный)
        '<ul>'
        # <li> - элементы списка
        '<li>Данные пользователей</li>'
        '<li>Статистика посещений</li>'
        '<li>Логи системы</li>'
        '</ul>'
        # <а> - ссылка на другую страницу
        # href="/test/" - указывает адрес ссылки
        '<a href="/test/">Перейти на тестовую страницу</a>'
    )


# Создаем функцию-представление для страницы /test
def test_page(request):
    """
    Эта функция будет вызываться, когда пользователь перейдет на страницу /test
    Возвращает другую HTML-страницу для тестирования
    """

    # Возвращаем другой HTTP-ответ с другим HTML-кодом
    return HttpResponse(
        # Здесь другой заголовок
        '<h1>Тестовая страница</h1>'
        # Здесь другой текст
        '<p>Это тестовая страница для проверки работы приложения.</p>'
        # Здесь другой список
        '<ol>'  # <ol> - упорядоченный список (нумерованный)
        '<li>Тест 1: Проверка загрузки</li>'
        '<li>Тест 2: Проверка ссылок</li>'
        '<li>Тест 3: Проверка отображения</li>'
        '</ol>'
        # Ссылка обратно на страницу с данными
        '<a href="/data/">Вернуться к данным</a>'
        # <br> - перенос строки
        '<br><br>'
        # Добавим немного цвета для наглядности
        '<div style="background-color: lightblue; padding: 20px;">'
        '<strong>Тест выполнен успешно!</strong>'
        '</div>'
    )
