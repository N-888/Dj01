# Импортируем функцию path из модуля django.urls
# path нужна для создания маршрутов URL
from django.urls import path

# Импортируем ВСЕ функции из файла views.py текущего приложения
# точка перед views означает "из текущей директории"
from . import views

# Создаем переменную app_name - это имя нашего приложения для URL
# Это нужно, чтобы Django понимал, к какому приложению относятся маршруты
app_name = 'myapp'

# Создаем список urlpatterns - это ОБЯЗАТЕЛЬНАЯ переменная для Django
# В этом списке будут храниться все маршруты нашего приложения
urlpatterns = [
    # Создаем первый маршрут для страницы /data
    # Первый параметр 'data/' - это часть URL после домена
    # Второй параметр views.data_page - это функция, которая будет вызвана
    # Третий параметр name='data' - это имя маршрута для использования в коде
    path('data/', views.data_page, name='data'),

    # Создаем второй маршрут для страницы /test
    # Аналогично первому, но для другого URL и другой функции
    path('test/', views.test_page, name='test'),
]
